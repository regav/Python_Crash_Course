# make sure to not put them in alpha order

locations = ['greece', 'colorado', 'japan', 'south korea', 'africa']
print("This is my list of places I wish to see.")
print(locations)
print("This is my list sorted.")
print(sorted(locations))
print("This is my original list")
print(locations)
locations.reverse()
print("This is my list in reverse order.")
print(locations)
locations.reverse()
print("This is my list again back in order.")
print(locations)
locations.sort()
print("This is my list sorted.")
print(locations)
locations.sort(reverse=True)
print("This is my list in reverse order.")
print(locations)